    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
// TODO add your handling code here:
        Thread thread = new Thread(new Runnable() {

            public void run() {
                try {

                    Analyzer analyzer = new Analyzer(p.getImage());
                    analyzer.progress = jProgressBar1;
                    analyzer.setMatrixPrecision(((Integer) matrixprecisionmodel.getValue()).intValue());
                    analyzer.Analyze();

                    int x = -1;
                    int i = -1;
                    Enumeration<AbstractButton> e = buttonGroup1.getElements();
                    while (e.hasMoreElements()) {
                        AbstractButton b = e.nextElement();
                        x++;
                        if (b.isSelected()) {
                            i = x;
                        }
                    }

                    p.loadTxt(analyzer, i);
                    p.repaint();
                } finally {
                    jDialog1.setVisible(false);
                }

            }
        });
        thread.setPriority(Thread.MIN_PRIORITY);

        thread.start();
        jDialog2.setVisible(false);
        jDialog1.setVisible(true);
    }//GEN-LAST:event_jButton5ActionPerformed

