    private void jPanel1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MousePressed
// TODO add your handling code here:
        if (jDialog2.isVisible()) {
            double z = ((Double) zoommodel.getValue()).doubleValue();
            jSpinner5.getModel().setValue(new Integer((int) ((double) evt.getX() / z)));
            jSpinner6.getModel().setValue(new Integer((int) ((double) evt.getY() / z)));

            if ((int) ((double) evt.getY() / z) > p.getImage().getHeight() / 2) {
                jSpinner7.getModel().setValue(new Integer(1));
            } else {
                jSpinner7.getModel().setValue(new Integer(-1));
            }

            jDialog2.setVisible(true);
        } else {
            if (p.mouseDown(evt, evt.getX(), evt.getY())) {
                jPanel1.repaint();
            }
            if (evt.getModifiers() == 12 /*alt+rightclick*/) {
                double z = ((Double) zoommodel.getValue()).doubleValue();
                int x = ((int) ((double) evt.getX() / z));
                int y = ((int) ((double) evt.getY() / z));
                int zz = 0;
                if ((int) ((double) evt.getY() / z) > p.getImage().getHeight() / 2) {
                    zz = 1;
                } else {
                    zz = -1;
                }
                p.addDEformatorsData(x, y, zz);
                jPanel1.repaint();
            }
        }
    }//GEN-LAST:event_jPanel1MousePressed

