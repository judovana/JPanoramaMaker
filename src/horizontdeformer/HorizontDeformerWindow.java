    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
// TODO add your handling code here:
        JFileChooser ofd = new JFileChooser();
        ofd.setDialogType(JFileChooser.SAVE_DIALOG);
        int returnVal = ofd.showSaveDialog(this);

        File fileName = ofd.getSelectedFile();

        if (fileName == null) {
            return;
        }
        if (returnVal != JFileChooser.APPROVE_OPTION) {
            return;
        }
        BufferedWriter f = null;
        try {
            f = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(fileName),"utf-8"));
            f.write(((Integer) jSpinner8.getModel().getValue()).toString());
            f.newLine();
            f.write("0");
            f.newLine();
            f.write("0");
            f.newLine();
            for (int x = 0; x < m.size(); x++) {
                TPoint localPoint = (TPoint) m.getElementAt(x);
                f.write(String.valueOf(localPoint.x));
                f.newLine();
                f.write(String.valueOf(localPoint.y));
                f.newLine();
                f.write(String.valueOf(localPoint.z));
                f.newLine();

            }


        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        } finally {
            if (f != null) {
                try {
                    f.close();
                } catch (IOException ex) {
                    ex.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton16ActionPerformed

    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        ArrayList tpoint = null;
// TODO add your handling code here:
        JFileChooser ofd = new JFileChooser();
        ofd.setDialogType(JFileChooser.OPEN_DIALOG);
        int returnVal = ofd.showOpenDialog(this);

        File fileName = ofd.getSelectedFile();

        if (fileName == null) {
            return;
        }
        if (returnVal != JFileChooser.APPROVE_OPTION) {
            return;
        }
        BufferedReader br = null;
        try {
            br =new BufferedReader(new InputStreamReader(new FileInputStream(fileName),"utf-8"));

            tpoint = new ArrayList();

            TPoint tp = new TPoint();
            String s = br.readLine();
            tp.x = Integer.valueOf(s);
            s = br.readLine();
            tp.y = Integer.valueOf(s);
            s = br.readLine();
            tp.z = Integer.valueOf(s);
            tpoint.add(tp);
            s = br.readLine();
            while (s != null) {
                tp = new TPoint();
                tpoint.add(tp);

                tp.x = Integer.valueOf(s);
                s = br.readLine();
                tp.y = Integer.valueOf(s);
                s = br.readLine();
                tp.z = Integer.valueOf(s);
                s = br.readLine();
            }


        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } catch (Exception ex) {
            ex.printStackTrace();
        } finally {
            if (br != null) {
                try {
                    br.close();
                } catch (IOException ex) {
                    ex.printStackTrace();

                }
            }
        }
        p.setPrimaryData(tpoint);
        m = new DefaultListModel();
        for (Iterator it = p.getPrimaryData().iterator(); it.hasNext();) {
            Object elem = (Object) it.next();
            m.addElement(elem);
        }
        jList1.setModel(m);
    }//GEN-LAST:event_jButton17ActionPerformed

